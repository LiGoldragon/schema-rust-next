use schema_next::{
    Asschema, EnumDeclaration, EnumVariant, Name, RootSurface, StructDeclaration, TypeDeclaration,
    TypeReference,
};

#[derive(Clone, Debug, Eq, PartialEq)]
pub struct GeneratedFile {
    pub path: String,
    pub code: RustCode,
}

#[derive(Clone, Debug, Eq, PartialEq)]
pub struct RustCode(String);

impl RustCode {
    pub fn as_str(&self) -> &str {
        &self.0
    }
}

#[derive(Clone, Debug, Default)]
pub struct RustEmitter;

impl RustEmitter {
    pub fn emit_file(&self, asschema: &Asschema) -> GeneratedFile {
        GeneratedFile {
            path: format!("{}.rs", asschema.identity().component().as_str()),
            code: self.emit(asschema),
        }
    }

    pub fn emit(&self, asschema: &Asschema) -> RustCode {
        let mut writer = RustWriter::default();
        writer.line("// @generated by schema-rust-next");
        writer.blank();
        writer.line("pub type Text = String;");
        writer.line("pub type Integer = u64;");
        writer.blank();

        for declaration in asschema.namespace() {
            writer.emit_type(declaration);
            writer.blank();
        }

        for surface in asschema.surfaces() {
            writer.emit_surface(surface);
            writer.blank();
        }

        writer.emit_short_headers(asschema.surfaces());
        RustCode(writer.finish())
    }
}

#[derive(Default)]
struct RustWriter {
    output: String,
}

impl RustWriter {
    fn line(&mut self, line: impl AsRef<str>) {
        self.output.push_str(line.as_ref());
        self.output.push('\n');
    }

    fn blank(&mut self) {
        self.output.push('\n');
    }

    fn finish(self) -> String {
        self.output
    }

    fn emit_type(&mut self, declaration: &TypeDeclaration) {
        match declaration {
            TypeDeclaration::Struct(declaration) => self.emit_struct(declaration),
            TypeDeclaration::Newtype(declaration) => self.emit_newtype(declaration),
            TypeDeclaration::Enum(declaration) => self.emit_enum(declaration),
        }
    }

    fn emit_newtype(&mut self, declaration: &StructDeclaration) {
        let field = declaration.fields.first().expect("newtype has one field");
        self.line("#[derive(rkyv::Archive, rkyv::Serialize, rkyv::Deserialize, Clone, Debug, PartialEq, Eq)]");
        self.line(format!(
            "pub struct {}(pub {});",
            declaration.name,
            rust_type(&field.reference)
        ));
    }

    fn emit_struct(&mut self, declaration: &StructDeclaration) {
        self.line("#[derive(rkyv::Archive, rkyv::Serialize, rkyv::Deserialize, Clone, Debug, PartialEq, Eq)]");
        self.line(format!("pub struct {} {{", declaration.name));
        for field in &declaration.fields {
            self.line(format!(
                "    pub {}: {},",
                field.name.as_str(),
                rust_type(&field.reference)
            ));
        }
        self.line("}");
    }

    fn emit_enum(&mut self, declaration: &EnumDeclaration) {
        self.line("#[derive(rkyv::Archive, rkyv::Serialize, rkyv::Deserialize, Clone, Debug, PartialEq, Eq)]");
        self.line(format!("pub enum {} {{", declaration.name));
        for variant in &declaration.variants {
            self.emit_variant(variant);
        }
        self.line("}");
    }

    fn emit_surface(&mut self, surface: &RootSurface) {
        self.line("#[derive(rkyv::Archive, rkyv::Serialize, rkyv::Deserialize, Clone, Debug, PartialEq, Eq)]");
        self.line(format!("pub enum {} {{", surface.name));
        for variant in &surface.variants {
            self.emit_variant(variant);
        }
        self.line("}");
    }

    fn emit_variant(&mut self, variant: &EnumVariant) {
        match &variant.payload {
            Some(reference) => {
                self.line(format!("    {}({}),", variant.name, rust_type(reference)))
            }
            None => self.line(format!("    {},", variant.name)),
        }
    }

    fn emit_short_headers(&mut self, surfaces: &[RootSurface]) {
        self.line("pub mod short_header {");
        for (surface_index, surface) in surfaces.iter().enumerate() {
            for (variant_index, variant) in surface.variants.iter().enumerate() {
                let constant = format!(
                    "{}_{}",
                    constant_name(&surface.name),
                    constant_name(&variant.name)
                );
                let value = ((surface_index as u64) << 56) | ((variant_index as u64) << 48);
                self.line(format!("    pub const {constant}: u64 = 0x{value:016X};"));
            }
        }
        self.line("}");
    }
}

fn rust_type(reference: &TypeReference) -> String {
    match reference.name.as_str() {
        "Text" => "Text".to_owned(),
        "Integer" => "Integer".to_owned(),
        name => name.to_owned(),
    }
}

fn constant_name(name: &Name) -> String {
    let mut output = String::new();
    for (index, character) in name.as_str().chars().enumerate() {
        if character.is_ascii_uppercase() {
            if index > 0 {
                output.push('_');
            }
            output.push(character);
        } else if character == '-' {
            output.push('_');
        } else {
            output.push(character.to_ascii_uppercase());
        }
    }
    output
}
